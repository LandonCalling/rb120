=begin
The benefits of OOP are such:
- Encapsulated data - the developer can control
  who access the data and how
- Allows grouping of behaviors that act on the
  same types of data
- Using objects allows the developer to think
  more abstractly about the code they are writing.
- Reduces duplication by creating reusable code.
- Easier to conceptualize because objects are nouns.
=end